	AREA	up_display_s,code
	


			
; -----------------------------------  Einsprungpunkt - --------------------------------------

up_display PROC

	PUSH {R0, R1}			

	LDR R2, =GPIOA_ODR     
	EOR R0, R0, R1			; Welcher Seite wird angezeigt
	STR R0, [R2]
	POP{R0, R1}				
	BX LR					
	
   ENDP

   END